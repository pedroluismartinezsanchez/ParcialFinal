        itemPagos.addActionListener(e -> {
            new FormPago().setVisible(true);
            dispose();
        });
        
        JMenuItem itemMascotas = new JMenuItem("Mascotas ðŸ¾");
        itemMascotas.addActionListener(e -> {
            FormMascota form = new FormMascota();
            form.setVisible(true);
        });
       
      

        JMenuItem itemPropietarios = new JMenuItem("Propietarios ðŸ§‘â€ðŸŒ¾");
        itemPropietarios.addActionListener(e -> {
            FormPropietario form = new FormPropietario();
            form.setVisible(true);
        });

        JMenuItem itemVeterinarios = new JMenuItem("Veterinarios ðŸ‘¨â€âš•ï¸");
        itemVeterinarios.addActionListener(e -> {
            FormVeterinario form = new FormVeterinario();
            form.setVisible(true);
        });

        menuGestion.add(itemMascotas);
        menuGestion.add(itemPropietarios);
        menuGestion.add(itemVeterinarios);
        menuHistorial.add(itemHistorial);
        
        
        
        barra.add(menuGestion);
        barra.add(menuHistorial);
        barra.add(itemConsulta);
        barra.add(itemReporte);
        barra.add(itemPagos);
        barra.add(itemSalir);
        
